package day5.loops;

public class SwrichCaseDemo {

	public static void main(String[] args) {
		
		int i=15;
		switch(i) {
		case 10:
			System.out.println("i is 10");
			break;
		case 15:
			System.out.println("i is 15");
			break;
		case 20:
			System.out.println("i is 20");
			break;
		default:
			System.out.println("i is not present");
			break;
		}
	}

}
